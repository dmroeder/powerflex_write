def get_parameter_name(drive, parameter):
    if drive == "PF4":
        return PF4[parameter]
    elif drive =="PF40":
        return PF40[parameter]
    elif drive ==  "PF40P":
        return PF40P[parameter]
    elif drive == "PF523":
        return PF523[parameter]
    elif drive == "PF525":
        return PF525[parameter]
    else:
        return "Unknown Drive Type: " + drive

